def _note(
    tenant_id: str, title: str, body: str, *, tags: list[str], note_type: str = "idea"
) -> str:
    tags_yaml = "[" + ", ".join(tags) + "]"
    return (
        "---\n"
        f"tenant_id: {tenant_id}\n"
        f"type: {note_type}\n"
        "status: growing\n"
        "source: demo-seed\n"
        f"tags: {tags_yaml}\n"
        "---\n\n"
        f"# {title}\n\n"
        f"{body}\n"
    )


