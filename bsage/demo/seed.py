def seed_demo_vault(vault_root: Path) -> int:
    """Write seed notes under ``vault_root``. Returns count written.

    A sentinel file under ``vault_root`` short-circuits subsequent calls
    so we never duplicate or overwrite visitor edits across restarts.
    """
    sentinel = vault_root / _SENTINEL
    if sentinel.exists():
        return 0

    vault_root.mkdir(parents=True, exist_ok=True)
    written = 0
    tenant_id = str(DEMO_SHARED_TENANT_ID)
    for rel, title, body, tags, note_type in _NOTES:
        target = vault_root / rel
        target.parent.mkdir(parents=True, exist_ok=True)
        target.write_text(_note(tenant_id, title, body, tags=tags, note_type=note_type))
        written += 1

    sentinel.write_text("seeded\n")
    logger.info("demo_vault_seeded", vault_root=str(vault_root), notes=written)
    return written
