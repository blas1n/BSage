"""BSage demo vault seeding — pre-populate the shared demo tenant's
vault with realistic notes so the visitor's vault, search, and graph
panels render real content instead of empty directories.

Runs at startup when ``BSVIBE_DEMO_MODE=true``. Idempotent: a sentinel
file under the vault root is checked first; subsequent boots skip the
seed entirely.

Notes carry ``tenant_id: <DEMO_SHARED_TENANT_ID>`` in their YAML
frontmatter so the existing tenant filter in ``bsage.gateway.routes``
admits them for demo visitors.
"""

